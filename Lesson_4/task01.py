# –¥–ª–∏–Ω–Ω–æ–≥–æ —Å–ª–æ–≤–∞ –±—ã–ª –æ–¥–∏–Ω –ø—Ä–æ–±–µ–ª –º–µ–∂–¥—É –Ω–∏–º –∏ –Ω–æ–º–µ—Ä–æ–º —Å—Ç—Ä–æ–∫–∏.

def print_new_row_word(str_: str):
    """–í—ã–≤–µ–¥–µ—Ç –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏ –≤ –∞–ª—Ñ–∞–≤–∏—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ —Å –Ω—É–º–µ—Ä–∞—Ü–∏–µ–π."""

    new_list = set(str_.replace('.', '').
                    replace(',', '').
                    replace(':', '').
                    replace('üî•', '').
                    replace('‚Äî', '').
                    replace('(', '').
                    replace(')', '').
                    replace('!', '').
                    replace('?', '').
                    replace('=', '').lower().split())
    max_word_len = len(max(new_list, key=len))
    for i, word in enumerate(sorted(new_list), 1):
        print(f'{i:<3}. {word:>{max_word_len}}')


